print("area of the figures")
print("1. Circle")
print("enter the radius of the circle:")
radius = float(input())
carea = 3.14 * radius * radius
print("area of the circle is", carea)
print("2. Rectangle")
print("enter the length and breadth of the rectangle:")
length = float(input())
breadth = float(input())
rarea = length * breadth
print("area of the rectangle is", rarea)
print("3. Triangle")
print("enter the base and height of the triangle:")
base = float(input())
height = float(input())
tarea = 0.5 * base * height
print("area of the triangle is", tarea)